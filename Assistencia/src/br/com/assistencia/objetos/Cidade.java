package br.com.assistencia.objetos;

public class Cidade {
	
	private String nome;
	private int idCidade;

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public int getIdCidade() {
		return idCidade;
	}

	public void setIdCidade(int idCidade) {
		this.idCidade = idCidade;
	}
	
	

}
