package br.com.assistencia.objetos;

public class Rua {
	
	private int idRua;
	private String nome;
	private String cep;
	
	public int getIdRua() {
		return idRua;
	}
	public void setIdRua(int idRua) {
		this.idRua = idRua;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getCep() {
		return cep;
	}
	public void setCep(String cep) {
		this.cep = cep;
	}
	
	
}
